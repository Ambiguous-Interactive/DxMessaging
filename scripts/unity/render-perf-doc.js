"use strict";

const fs = require("fs");
const path = require("path");

const { extractRows } = require("./extract-perf-baseline.js");
const {
  SCENARIO_ORDER,
  DISPATCH_DISPLAY_NAMES,
  COMPARISON_SCENARIO_ORDER,
  COMPARISON_SCENARIO_LABELS,
  COMPARISON_TECH_ORDER,
  COMPARISON_TECH_LABELS,
  isComparisonScenario,
  parseComparisonScenario,
  deriveScope
} = require("./perf-scenarios.js");

// render-perf-doc.js owns this docs/architecture/performance.md region.
const BEGIN_MARKER = "<!-- AUTOGENERATED:DISPATCH-THROUGHPUT BEGIN -->";
const END_MARKER = "<!-- AUTOGENERATED:DISPATCH-THROUGHPUT END -->";

// Wall-clock scenarios report milliseconds, not emits/sec.
const REGISTRATION_SCENARIOS = new Set([
  "RegistrationFlood_1000Types_FromColdBus",
  "RegistrationFlood_1000Types_WarmJit",
  "DeregistrationFlood_1000Types_Cold",
  "DeregistrationFlood_1000Types_WarmJit",
  "UntargetedFirstDispatch_Cold",
  "TargetedFirstDispatch_Cold",
  "BroadcastFirstDispatch_Cold"
]);

// Headline order: Standalone drives throughput/provenance; profiler metrics use
// the first scope that measured that specific metric.
const SCOPE_ORDER = ["Standalone", "PlayMode", "EditMode"];

// Fallback backend labels; scope headings prefer the backend token in platform rows.
const SCOPE_BACKEND = { Standalone: "IL2CPP", PlayMode: "Mono", EditMode: "Mono" };

// Treat runs within this relative tolerance as unchanged to avoid jitter churn.
const DEFAULT_TOLERANCE = 0.02;

// Privacy-safe fallback when --machine-specs is absent or unreadable.
const NEUTRAL_RUNNER_DESCRIPTION = "self-hosted Windows runner";

function usage() {
  return `Usage: node scripts/unity/render-perf-doc.js --input <unity-log-or-results> [--input <path> ...] --doc <performance.md> --unity-version <version> [--platform-substring <s>] [--tolerance <fraction>] [--machine-specs <json>] [--check]

Extracts dispatch-throughput and library-comparison rows for a single Unity
version, renders deterministic per-scope tables plus cross-library comparison
matrices, and rewrites the AUTOGENERATED region of the performance doc in place.

  --input             Unity log or NUnit results file (repeatable).
  --doc               Target markdown file containing the AUTOGENERATED markers.
  --unity-version     Unity version whose rows drive the tables (e.g. 6000.3.16f1).
  --platform-substring Override the platform filter (default: "Unity <version>").
  --tolerance         Relative jitter tolerance for idempotence (default ${DEFAULT_TOLERANCE}).
  --machine-specs     Optional collect-machine-specs.ps1 JSON; embeds a Runner
                      provenance line (falls back to a neutral literal if absent).
  --check             Do not write; exit 3 if the doc would change. Useful in CI drift checks.

When the rendered region matches the existing region (within tolerance for
numeric cells), the file is left byte-for-byte unchanged and the process exits 0.
`;
}

function parseArgs(argv) {
  const options = {
    inputs: [],
    doc: "",
    unityVersion: "",
    platformSubstring: "",
    tolerance: DEFAULT_TOLERANCE,
    machineSpecs: "",
    check: false,
    help: false
  };

  for (let index = 2; index < argv.length; index++) {
    const arg = argv[index];
    if (arg === "--input") {
      options.inputs.push(requireValue(argv, ++index, arg));
      continue;
    }
    if (arg === "--doc") {
      options.doc = requireValue(argv, ++index, arg);
      continue;
    }
    if (arg === "--unity-version") {
      options.unityVersion = requireValue(argv, ++index, arg);
      continue;
    }
    if (arg === "--platform-substring") {
      options.platformSubstring = requireValue(argv, ++index, arg);
      continue;
    }
    if (arg === "--tolerance") {
      options.tolerance = parseTolerance(requireValue(argv, ++index, arg));
      continue;
    }
    if (arg === "--machine-specs") {
      options.machineSpecs = requireValue(argv, ++index, arg);
      continue;
    }
    if (arg === "--check") {
      options.check = true;
      continue;
    }
    if (arg === "--help" || arg === "-h") {
      options.help = true;
      continue;
    }
    throw new Error(`Unknown argument: ${arg}`);
  }

  return options;
}

function requireValue(argv, index, flag) {
  const value = argv[index];
  if (value === undefined || value.startsWith("--")) {
    throw new Error(`${flag} requires a value.`);
  }
  return value;
}

function parseTolerance(value) {
  const parsed = Number.parseFloat(value);
  if (!Number.isFinite(parsed) || parsed < 0) {
    throw new Error(`--tolerance must be a non-negative number, got: ${value}`);
  }
  return parsed;
}

function readInputs(inputs) {
  if (inputs.length === 0) {
    throw new Error("At least one --input path is required.");
  }
  return inputs
    .map((inputPath) => {
      if (!fs.existsSync(inputPath)) {
        throw new Error(`Input file not found: ${inputPath}`);
      }
      return fs.readFileSync(inputPath, "utf8");
    })
    .join("\n");
}

// Keep rows separated by execution scope; first row per scope/scenario wins.
function selectRowsForVersion(rows, platformSubstring) {
  const matching = rows.filter((row) => row.platform.includes(platformSubstring));

  const dispatchByScope = new Map();
  const comparisonByScope = new Map();

  for (const row of matching) {
    const scope = deriveScope(row.platform);
    if (!scope) {
      continue;
    }

    if (isComparisonScenario(row.scenario)) {
      const parsed = parseComparisonScenario(row.scenario);
      if (!parsed) {
        continue;
      }
      if (!comparisonByScope.has(scope)) {
        comparisonByScope.set(scope, new Map());
      }
      const byCell = comparisonByScope.get(scope);
      const cellKey = `${parsed.techKey}|${parsed.scenarioKey}`;
      if (!byCell.has(cellKey)) {
        byCell.set(cellKey, row);
      }
      continue;
    }

    if (!SCENARIO_ORDER.includes(row.scenario)) {
      continue;
    }
    if (!dispatchByScope.has(scope)) {
      dispatchByScope.set(scope, new Map());
    }
    const byScenario = dispatchByScope.get(scope);
    if (!byScenario.has(row.scenario)) {
      byScenario.set(row.scenario, row);
    }
  }

  const scopesPresent = SCOPE_ORDER.filter((scope) => dispatchByScope.has(scope));

  return { dispatchByScope, comparisonByScope, scopesPresent };
}

function formatThroughput(emitsPerSecond) {
  const millions = Number.parseFloat(emitsPerSecond) / 1_000_000;
  return `${millions.toFixed(2)} M emits/sec`;
}

function formatWallClock(wallClockMs) {
  return `${Number.parseFloat(wallClockMs).toFixed(3)} ms`;
}

// -1 is the unmeasured sentinel; measured zero stays a real zero.
function formatAllocations(gcAllocations) {
  const count = Number.parseInt(gcAllocations, 10);
  if (!Number.isFinite(count) || count < 0) {
    return "n/a";
  }
  return count.toLocaleString("en-US");
}

function formatBytesAllocated(gcAllocatedBytes) {
  const bytes = Number.parseInt(gcAllocatedBytes, 10);
  if (!Number.isFinite(bytes) || bytes < 0) {
    return "n/a";
  }
  return bytes.toLocaleString("en-US");
}

function formatRowAllocations(row) {
  return formatAllocations(row.gcAllocations);
}

function formatRowBytes(row) {
  return formatBytesAllocated(row.gcAllocatedBytes);
}

// Optional runner diagnostics fall back to neutral text when missing or malformed.
function readMachineSpecs(machineSpecsPath) {
  if (!machineSpecsPath) {
    return NEUTRAL_RUNNER_DESCRIPTION;
  }
  let raw;
  try {
    raw = fs.readFileSync(machineSpecsPath, "utf8");
  } catch {
    return NEUTRAL_RUNNER_DESCRIPTION;
  }
  let specs;
  try {
    specs = JSON.parse(raw);
  } catch {
    return NEUTRAL_RUNNER_DESCRIPTION;
  }
  if (!specs || typeof specs !== "object") {
    return NEUTRAL_RUNNER_DESCRIPTION;
  }
  return formatMachineSpecs(specs);
}

// Missing fields render as "unknown" so partial diagnostics never leak undefined.
function formatMachineSpecs(specs) {
  const field = (value) => {
    if (value === undefined || value === null) {
      return "unknown";
    }
    const text = String(value).trim();
    return text.length === 0 ? "unknown" : text;
  };
  const cpu = field(specs.cpu);
  const physicalCores = field(specs.physicalCores);
  const logicalCores = field(specs.logicalCores);
  const clockMhz = field(specs.clockMhz);
  const ramGb = field(specs.ramGb);
  const ramType = field(specs.ramType);
  const ramSpeedMhz = field(specs.ramSpeedMhz);
  const gpu = field(specs.gpu);
  const os = field(specs.os);
  return (
    `${cpu}, ${physicalCores}C/${logicalCores}T @ ${clockMhz}MHz; ` +
    `${ramGb}GB ${ramType}@${ramSpeedMhz}; ${gpu}; ${os}`
  );
}

// Match Prettier's GFM table alignment so generated docs stay byte-stable.
function alignTable(rows) {
  const columnCount = rows[0].length;
  const widths = new Array(columnCount).fill(3);
  for (const cells of rows) {
    for (let column = 0; column < columnCount; column++) {
      widths[column] = Math.max(widths[column], cells[column].length);
    }
  }

  const renderRow = (cells) =>
    `| ${cells.map((cell, column) => cell.padEnd(widths[column])).join(" | ")} |`;
  const separator = `| ${widths.map((width) => "-".repeat(width)).join(" | ")} |`;

  const [header, ...body] = rows;
  return [renderRow(header), separator, ...body.map(renderRow)].join("\n");
}

function buildDispatchTable(byScenario) {
  const selected = [];
  for (const scenario of SCENARIO_ORDER) {
    const row = byScenario.get(scenario);
    if (row) {
      selected.push({ scenario, row });
    }
  }
  if (selected.length === 0) {
    return null;
  }

  // Omit all-unmeasured profiler columns; keep partial n/a cells as signal.
  const hasAllocations = selected.some(({ row }) => isMeasuredInteger(row.gcAllocations));
  const hasBytes = selected.some(({ row }) => isMeasuredInteger(row.gcAllocatedBytes));

  const header = ["Scenario", "Throughput / Wall clock"];
  if (hasAllocations) {
    header.push("GC allocs");
  }
  if (hasBytes) {
    header.push("GC bytes");
  }

  const dataRows = selected.map(({ scenario, row }) => {
    const primary = REGISTRATION_SCENARIOS.has(scenario)
      ? formatWallClock(row.wallClockMs)
      : formatThroughput(row.emitsPerSecond);
    const cells = [DISPATCH_DISPLAY_NAMES[scenario] || scenario, primary];
    if (hasAllocations) {
      cells.push(formatAllocations(row.gcAllocations));
    }
    if (hasBytes) {
      cells.push(formatBytesAllocated(row.gcAllocatedBytes));
    }
    return cells;
  });

  return alignTable([header, ...dataRows]);
}

function buildDispatchSections(dispatchByScope, scopesPresent) {
  const sections = [];
  for (const scope of scopesPresent) {
    const byScenario = dispatchByScope.get(scope);
    const table = buildDispatchTable(byScenario);
    if (!table) {
      continue;
    }
    const sampleRow = SCENARIO_ORDER.map((scenario) => byScenario.get(scenario)).find(Boolean);
    const platform = sampleRow ? sampleRow.platform : "unknown";
    sections.push([
      `### Dispatch throughput - ${scopeLabel(scope, platform)}`,
      "",
      `Platform: ${platform}.`,
      "",
      table
    ]);
  }
  return sections;
}

function preferredComparisonScope(comparisonByScope) {
  for (const scope of SCOPE_ORDER) {
    const byCell = comparisonByScope.get(scope);
    if (byCell && byCell.size > 0) {
      return scope;
    }
  }
  return null;
}

function isMeasuredInteger(value) {
  const parsed = Number.parseInt(value, 10);
  return Number.isFinite(parsed) && parsed >= 0;
}

function preferredComparisonMetricScope(comparisonByScope, selectMetric) {
  for (const scope of SCOPE_ORDER) {
    const byCell = comparisonByScope.get(scope);
    if (!byCell) {
      continue;
    }
    for (const row of byCell.values()) {
      if (isMeasuredInteger(selectMetric(row))) {
        return scope;
      }
    }
  }
  return preferredComparisonScope(comparisonByScope);
}

function anyScopeMeasures(comparisonByScope, selectMetric) {
  for (const byCell of comparisonByScope.values()) {
    for (const row of byCell.values()) {
      if (isMeasuredInteger(selectMetric(row))) {
        return true;
      }
    }
  }
  return false;
}

function preferredComparisonAllocScope(comparisonByScope) {
  return preferredComparisonMetricScope(comparisonByScope, (row) => row.gcAllocations);
}

function preferredComparisonBytesScope(comparisonByScope) {
  return preferredComparisonMetricScope(comparisonByScope, (row) => row.gcAllocatedBytes);
}

function buildMetricComparison(
  comparisonByScope,
  throughputScope,
  throughputByCell,
  scope,
  formatCell
) {
  let selectedScope = scope;
  let byCell = comparisonByScope.get(selectedScope);
  let table = byCell ? buildComparisonMatrix(byCell, formatCell, { bold: false }) : null;

  // Defense-in-depth: never push a null metric table.
  if (!table) {
    selectedScope = throughputScope;
    byCell = throughputByCell;
    table = buildComparisonMatrix(byCell, formatCell, { bold: false });
  }

  const sample = byCell.values().next().value;
  return {
    scope: selectedScope,
    platform: sample ? sample.platform : undefined,
    table
  };
}

// Throughput matrices bold per-scenario display-precision winners.
function buildComparisonMatrix(byCell, formatCell, { bold }) {
  const techsPresent = COMPARISON_TECH_ORDER.filter((techKey) =>
    COMPARISON_SCENARIO_ORDER.some((scenarioKey) => byCell.has(`${techKey}|${scenarioKey}`))
  );
  if (techsPresent.length === 0) {
    return null;
  }

  const header = [
    "Technology",
    ...COMPARISON_SCENARIO_ORDER.map((key) => COMPARISON_SCENARIO_LABELS[key])
  ];

  // Display-precision ties all win, reducing churn for near-tied techs.
  const winningCellByScenario = new Map();
  if (bold) {
    for (const scenarioKey of COMPARISON_SCENARIO_ORDER) {
      let best = -Infinity;
      for (const techKey of techsPresent) {
        const row = byCell.get(`${techKey}|${scenarioKey}`);
        if (!row) {
          continue;
        }
        const emitsPerSecond = Number.parseFloat(row.emitsPerSecond);
        if (Number.isFinite(emitsPerSecond) && emitsPerSecond > best) {
          best = emitsPerSecond;
        }
      }
      if (best > 0) {
        winningCellByScenario.set(scenarioKey, formatThroughput(best));
      }
    }
  }

  const dataRows = [header];
  for (const techKey of techsPresent) {
    const cells = [COMPARISON_TECH_LABELS[techKey] || techKey];
    for (const scenarioKey of COMPARISON_SCENARIO_ORDER) {
      const row = byCell.get(`${techKey}|${scenarioKey}`);
      if (!row) {
        cells.push("N/A");
        continue;
      }
      const formatted = formatCell(row);
      const isWinner = bold && winningCellByScenario.get(scenarioKey) === formatted;
      cells.push(isWinner ? `**${formatted}**` : formatted);
    }
    dataRows.push(cells);
  }

  return alignTable(dataRows);
}

function buildComparisonSections(comparisonByScope) {
  const throughputScope = preferredComparisonScope(comparisonByScope);
  if (!throughputScope) {
    return [];
  }
  const throughputByCell = comparisonByScope.get(throughputScope);
  const throughputSample = throughputByCell.values().next().value;
  const throughputPlatform = throughputSample ? throughputSample.platform : undefined;
  const throughputTable = buildComparisonMatrix(
    throughputByCell,
    (row) => formatThroughput(row.emitsPerSecond),
    { bold: true }
  );
  if (!throughputTable) {
    return [];
  }

  const sections = [
    [
      `### Library comparison - throughput (${scopeLabel(throughputScope, throughputPlatform)})`,
      "",
      throughputTable
    ]
  ];

  // Omit all-unmeasured metric matrices; keep partial n/a cells as signal.
  if (anyScopeMeasures(comparisonByScope, (row) => row.gcAllocations)) {
    const allocations = buildMetricComparison(
      comparisonByScope,
      throughputScope,
      throughputByCell,
      preferredComparisonAllocScope(comparisonByScope),
      formatRowAllocations
    );
    sections.push([
      `### Library comparison - GC allocations per 10k ops (${scopeLabel(allocations.scope, allocations.platform)})`,
      "",
      allocations.table
    ]);
  }
  if (anyScopeMeasures(comparisonByScope, (row) => row.gcAllocatedBytes)) {
    const bytes = buildMetricComparison(
      comparisonByScope,
      throughputScope,
      throughputByCell,
      preferredComparisonBytesScope(comparisonByScope),
      formatRowBytes
    );
    sections.push([
      `### Library comparison - GC allocated bytes per 10k ops (${scopeLabel(bytes.scope, bytes.platform)})`,
      "",
      bytes.table
    ]);
  }

  return sections;
}

// Prefer the backend token emitted in the benchmark platform string.
function deriveBackendLabel(platform) {
  if (typeof platform === "string") {
    if (/\bIL2CPP\b/i.test(platform)) {
      return "IL2CPP";
    }
    if (/\bMono\b/i.test(platform)) {
      return "Mono";
    }
  }
  return null;
}

function scopeLabel(scope, platform) {
  const backend = deriveBackendLabel(platform) || SCOPE_BACKEND[scope] || "Mono";
  return `${scope} (${backend})`;
}

// Keep provenance outside tables so commit/spec churn does not force rewrites.
function buildBlock(selection, unityVersion, { runnerDescription = "" } = {}) {
  const { dispatchByScope, comparisonByScope, scopesPresent } = selection;

  const dispatchSections = buildDispatchSections(dispatchByScope, scopesPresent);
  if (dispatchSections.length === 0) {
    throw new Error(
      `No DispatchThroughputBenchmarks rows matched Unity ${unityVersion}. ` +
        "Check the --unity-version / --platform-substring filter and the input logs."
    );
  }
  const comparisonSections = buildComparisonSections(comparisonByScope);

  const commit = firstSelectedCommit(selection);

  const lines = [
    BEGIN_MARKER,
    "",
    "<!-- Do not edit by hand. Rewritten by scripts/unity/render-perf-doc.js from",
    "     the latest Unity version's benchmark run. See perf-numbers.yml. -->",
    "",
    `Latest CI benchmark run: Unity ${unityVersion}, commit \`${commit}\`.`
  ];

  if (runnerDescription) {
    lines.push("");
    lines.push(`Runner: ${runnerDescription}`);
  }

  for (const section of [...dispatchSections, ...comparisonSections]) {
    lines.push("");
    lines.push(...section);
  }

  lines.push("");
  lines.push(END_MARKER);
  return lines.join("\n");
}

function firstSelectedCommit(selection) {
  for (const scope of selection.scopesPresent) {
    const byScenario = selection.dispatchByScope.get(scope);
    for (const scenario of SCENARIO_ORDER) {
      const row = byScenario.get(scenario);
      if (row) {
        return row.commit;
      }
    }
  }
  return "unknown";
}

function countOccurrences(content, needle) {
  let count = 0;
  let from = 0;
  for (;;) {
    const at = content.indexOf(needle, from);
    if (at === -1) {
      return count;
    }
    count += 1;
    from = at + needle.length;
  }
}

function locateMarkers(content) {
  const beginIndex = content.indexOf(BEGIN_MARKER);
  const endIndex = content.indexOf(END_MARKER);
  if (beginIndex === -1 || endIndex === -1) {
    throw new Error(
      `Could not find AUTOGENERATED dispatch-throughput markers in the doc. ` +
        `Expected both '${BEGIN_MARKER}' and '${END_MARKER}'.`
    );
  }
  // Refuse duplicate regions; first-match slicing would leave stale generated docs.
  const beginCount = countOccurrences(content, BEGIN_MARKER);
  const endCount = countOccurrences(content, END_MARKER);
  if (beginCount !== 1 || endCount !== 1) {
    throw new Error(
      `Expected exactly one AUTOGENERATED dispatch-throughput marker pair, ` +
        `found ${beginCount} '${BEGIN_MARKER}' and ${endCount} '${END_MARKER}'. ` +
        "Remove the duplicate markers so the renderer owns a single region."
    );
  }
  if (endIndex < beginIndex) {
    throw new Error("AUTOGENERATED END marker appears before BEGIN marker in the doc.");
  }
  return { beginIndex, endIndex: endIndex + END_MARKER.length };
}

function replaceBlock(content, block) {
  const { beginIndex, endIndex } = locateMarkers(content);
  return content.slice(0, beginIndex) + block + content.slice(endIndex);
}

function extractCellNumbers(text) {
  const matches = text.match(/-?\d[\d,]*(?:\.\d+)?/g);
  if (!matches) {
    return [];
  }
  return matches.map((token) => Number.parseFloat(token.replace(/,/g, "")));
}

function stripNumbers(text) {
  return text.replace(/-?\d[\d,]*(?:\.\d+)?/g, "#");
}

// Compare only table rows, cell-by-cell, with numeric tolerance and stable order.
function tableRowCells(line) {
  const trimmed = line.trim();
  if (!trimmed.startsWith("|")) {
    return null;
  }
  return (
    trimmed
      .replace(/^\|/, "")
      .replace(/\|$/, "")
      .split("|")
      // Bold markers are presentation; numbers still tolerance-compare.
      .map((cell) => cell.trim().replace(/^\*\*(.*)\*\*$/, "$1"))
  );
}

function isSeparatorCells(cells) {
  return cells.every((cell) => /^:?-+:?$/.test(cell));
}

function tableRows(block) {
  return block
    .split(/\r?\n/)
    .map(tableRowCells)
    .filter((cells) => cells !== null);
}

function rowsEquivalent(existingCells, candidateCells, tolerance) {
  if (existingCells.length !== candidateCells.length) {
    return false;
  }
  // Separator rows differ only in dash count once Prettier aligns the table;
  // treat any two well-formed separator rows of equal arity as equal.
  if (isSeparatorCells(existingCells) && isSeparatorCells(candidateCells)) {
    return true;
  }
  for (let column = 0; column < existingCells.length; column++) {
    const existingCell = existingCells[column];
    const candidateCell = candidateCells[column];
    if (stripNumbers(existingCell) !== stripNumbers(candidateCell)) {
      return false;
    }
    const existingNumbers = extractCellNumbers(existingCell);
    const candidateNumbers = extractCellNumbers(candidateCell);
    if (existingNumbers.length !== candidateNumbers.length) {
      return false;
    }
    for (let n = 0; n < existingNumbers.length; n++) {
      if (!withinTolerance(existingNumbers[n], candidateNumbers[n], tolerance)) {
        return false;
      }
    }
  }
  return true;
}

function blocksEquivalent(existingBlock, candidateBlock, tolerance) {
  const existingRows = tableRows(existingBlock);
  const candidateRows = tableRows(candidateBlock);
  if (existingRows.length === 0 || existingRows.length !== candidateRows.length) {
    return false;
  }
  for (let index = 0; index < existingRows.length; index++) {
    if (!rowsEquivalent(existingRows[index], candidateRows[index], tolerance)) {
      return false;
    }
  }
  return true;
}

function withinTolerance(existing, candidate, tolerance) {
  if (existing === candidate) {
    return true;
  }
  const denominator = Math.max(Math.abs(existing), Math.abs(candidate));
  if (denominator === 0) {
    return true;
  }
  return Math.abs(existing - candidate) / denominator <= tolerance;
}

function extractExistingBlock(content) {
  const { beginIndex, endIndex } = locateMarkers(content);
  return content.slice(beginIndex, endIndex);
}

function render(options) {
  const rows = extractRows(readInputs(options.inputs));
  if (rows.length === 0) {
    throw new Error("No DispatchThroughputBenchmarks rows found in the inputs.");
  }

  const platformSubstring =
    options.platformSubstring || `Unity ${requireUnityVersion(options.unityVersion)}`;
  const selection = selectRowsForVersion(rows, platformSubstring);
  const runnerDescription = readMachineSpecs(options.machineSpecs);
  const candidateBlock = buildBlock(selection, options.unityVersion, { runnerDescription });

  const originalContent = fs.readFileSync(options.doc, "utf8");
  const existingBlock = extractExistingBlock(originalContent);

  if (blocksEquivalent(existingBlock, candidateBlock, options.tolerance)) {
    return { changed: false, content: originalContent };
  }

  const updatedContent = replaceBlock(originalContent, candidateBlock);
  return { changed: true, content: updatedContent };
}

function requireUnityVersion(value) {
  if (!value) {
    throw new Error("--unity-version is required (or pass --platform-substring explicitly).");
  }
  return value;
}

function main(argv = process.argv) {
  const options = parseArgs(argv);
  if (options.help) {
    process.stdout.write(usage());
    return 0;
  }
  if (!options.doc) {
    throw new Error("--doc is required.");
  }
  if (!fs.existsSync(options.doc)) {
    throw new Error(`Doc file not found: ${options.doc}`);
  }

  const result = render(options);

  if (options.check) {
    if (result.changed) {
      process.stderr.write(
        `Performance doc is out of date: ${path.relative(process.cwd(), options.doc)} ` +
          "would change. Re-run render-perf-doc.js without --check (or wait for the " +
          "perf-numbers.yml default-branch refresh run) to refresh it.\n"
      );
      return 3;
    }
    process.stderr.write("Performance doc is up to date.\n");
    return 0;
  }

  if (!result.changed) {
    process.stderr.write("Performance doc already up to date; no write performed.\n");
    return 0;
  }

  fs.writeFileSync(options.doc, result.content, "utf8");
  process.stderr.write(`Updated dispatch-throughput tables in ${options.doc}.\n`);
  return 0;
}

if (require.main === module) {
  try {
    process.exitCode = main();
  } catch (error) {
    process.stderr.write(`${error.message}\n\n${usage()}`);
    process.exitCode = 1;
  }
}

